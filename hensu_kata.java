public class hensu_kata {
  
  public static void main(String[] args) {
    //byte型(-128 ～ 127までの数が格納できる)変数宣言を代入
    byte b=127;
    System.out.println(b);

    //short型(-32768 ～ 32767までの数が格納できる)変数の宣言を代入
    short s=32767;
    System.out.println(s);

    //int型変数の宣言を代入
    int i=21456756;
    System.out.println(i);

    //long型変数の宣言を代入
    long l=777777777777L;
    System.out.println(l);

    //float型(小数)変数に宣言を代入
    float f=3.14159265358979323846F;
    System.out.println(f);

    //double型変数の宣言を代入
    double d=3.14159265358979323846;
    System.out.println(d);

    //boolean型(true,falseしか入らない)変数の宣言を代入
    boolean bool=true;
    System.out.println(bool);

    //char(ｷｬﾗ)型(一文字のみ代入が可能)変数の宣言を代入
    char c='A';
    System.out.println(c);
  }
}
